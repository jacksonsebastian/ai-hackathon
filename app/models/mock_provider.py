class MockProvider(ModelProvider):
    """
    Mock LLM provider for local development.
    
    Simulates LLM responses with realistic delays and
    pre-built templates. No GPU required.
    """

    def __init__(self, simulate_delay: bool = True, delay_range: tuple = (0.3, 1.0)):
        self.simulate_delay = simulate_delay
        self.delay_range = delay_range
        self.call_count = 0
        logger.info("MockProvider initialized (local dev mode)")

    async def _simulate_delay(self):
        if self.simulate_delay:
            delay = random.uniform(*self.delay_range)
            await asyncio.sleep(delay)

    def _get_mock_response(self, prompt: str, system_prompt: str = "") -> str:
        prompt_lower = prompt.lower()
        sys_lower = system_prompt.lower()
        combined = f"{sys_lower} {prompt_lower}"

        if any(w in combined for w in ("evaluat", "score", "assess", "rate")):
            return MOCK_RESPONSES["evaluation"]
        if any(w in combined for w in ("resume", "parse", "extract", "candidate profile")):
            return MOCK_RESPONSES["resume_analysis"]
        if any(w in combined for w in ("feedback", "report", "recommendation", "roadmap")):
            return MOCK_RESPONSES["feedback_report"]
        if any(w in combined for w in ("code", "implement", "algorithm", "function")):
            return random.choice(MOCK_RESPONSES["coding_question"])
        if any(w in combined for w in ("behavioral", "leadership", "teamwork", "conflict")):
            return random.choice(MOCK_RESPONSES["behavioral_question"])
        if any(w in combined for w in ("technical", "system design", "database", "dsa")):
            return random.choice(MOCK_RESPONSES["technical_question"])
        return MOCK_RESPONSES["default"]

    async def generate(
        self,
        prompt: str,
        system_prompt: str = "",
        config: Optional[GenerationConfig] = None,
    ) -> GenerationResult:
        self.call_count += 1
        start = time.time()
        await self._simulate_delay()
        text = self._get_mock_response(prompt, system_prompt)
        elapsed = (time.time() - start) * 1000

        return GenerationResult(
            text=text,
            model="mock-provider-v1",
            tokens_used=len(text) // 4,
            prompt_tokens=len(prompt) // 4,
            completion_tokens=len(text) // 4,
            latency_ms=elapsed,
            finish_reason="stop",
        )

    async def generate_stream(
        self,
        prompt: str,
        system_prompt: str = "",
        config: Optional[GenerationConfig] = None,
    ) -> AsyncIterator[str]:
        self.call_count += 1
        text = self._get_mock_response(prompt, system_prompt)
        words = text.split()
        for word in words:
            if self.simulate_delay:
                await asyncio.sleep(random.uniform(0.02, 0.08))
            yield word + " "

    async def generate_structured(
        self,
        prompt: str,
        system_prompt: str = "",
        output_schema: Optional[dict] = None,
        config: Optional[GenerationConfig] = None,
    ) -> dict:
        result = await self.generate(prompt, system_prompt, config)
        try:
            return json.loads(result.text)
        except json.JSONDecodeError:
            return {"response": result.text}

    def get_model_info(self) -> dict:
        return {
            "provider": "mock",
            "model": "mock-provider-v1",
            "environment": "local",
            "gpu_required": False,
            "total_calls": self.call_count,
        }

    async def health_check(self) -> bool:
        return True
